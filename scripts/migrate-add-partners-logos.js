import 'dotenv/config';
import mongoose from 'mongoose';

const MONGODB_URI = process.env.MONGODB_URI;
if (!MONGODB_URI) {
    console.error('вќЊ  MONGODB_URI is not set.');
    process.exit(1);
}

const SiteContentSchema = new mongoose.Schema({
    conference: { type: String, default: 'liutex', index: true },
    key:        { type: String, required: true },
    data:       { type: mongoose.Schema.Types.Mixed, required: true },
    updatedAt:  { type: Date, default: Date.now },
});
SiteContentSchema.index({ conference: 1, key: 1 }, { unique: true });
const SiteContent = mongoose.models.SiteContent || mongoose.model('SiteContent', SiteContentSchema);

const DEFAULT_DATA = {
    title: 'Promoting & Media Partners',
    items: [],
};

async function migrate() {
    console.log('\nрџ”—  Connecting to MongoDBвЂ¦');
    await mongoose.connect(MONGODB_URI);
    console.log('вњ…  Connected\n');

    // Find all distinct conference IDs that have any SiteContent
    const conferences = await SiteContent.distinct('conference');
    console.log(`рџ“‹  Found ${conferences.length} conference(s): ${conferences.join(', ')}\n`);

    let created = 0;
    let skipped = 0;

    for (const conf of conferences) {
        const existing = await SiteContent.findOne({ conference: conf, key: 'partners_logos' });
        if (existing) {
            console.log(`  вЏ©  SKIPPED  [${conf}] вЂ” partners_logos already exists`);
            skipped++;
        } else {
            await SiteContent.create({ conference: conf, key: 'partners_logos', data: DEFAULT_DATA });
            console.log(`  вњ…  CREATED  [${conf}] вЂ” partners_logos`);
            created++;
        }
    }

    console.log(`\nв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`);
    console.log(`  Created : ${created}`);
    console.log(`  Skipped : ${skipped}`);
    console.log(`  Total   : ${conferences.length}`);
    console.log(`в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ\n`);

    await mongoose.disconnect();
    console.log('рџ”Њ  Disconnected. Done!\n');
}

migrate().catch(err => {
    console.error('вќЊ  Migration failed:', err.message);
    process.exit(1);
});
